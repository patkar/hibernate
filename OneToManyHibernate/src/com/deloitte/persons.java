package com.deloitte;

public class persons {
	int pid;
	String name;
	passport pp;
	
	public passport getPp() {
		return pp;
	}
	public void setPp(passport pp) {
		this.pp = pp;
	}
	public int getPid() {
		return pid;
	}
	public void setPid(int pid) {
		this.pid = pid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
